    private void jButtonsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsaveActionPerformed
        try {
            // TODO add your handling code here:
            int page, acc, ddc, year;
            String title = jTextFieldtitle.getText();
            String author = jTextFieldauthor.getText();
            String b_edition = jTextFieldedition.getText();
            String publisher = jTextFieldpublisher.getText();
            String desc = jTextFielddesc.getText();
            String sdate = jTextFieldsdate.getText();
            String exdate = jTextFieldexdate.getText();

            if (id != 0 && !"".equals(jTextFieldaccno.getText()) && !"".equals(jTextFieldauth_mark.getText()) && !"".equals(jTextFieldddc_no.getText())) {
                page = Integer.parseInt(jTextFieldpages.getText());
                acc = Integer.parseInt(jTextFieldaccno.getText());
                ddc = Integer.parseInt(jTextFieldddc_no.getText());
                year = Integer.parseInt(jTextFieldyear.getText());
                String b_authormark = jTextFieldauth_mark.getText();

                if (sdate.length() > 0) {
                    sdate = "STR_TO_DATE('" + sdate + "','%d %M %Y')";
                } else {
                    sdate = "curdate()";
                }

                if (exdate.length() > 0) {
                    exdate = "STR_TO_DATE('" + exdate + "','%d %M %Y')";
                } else {
                    exdate = "null";
                }

                String query = "Insert into Book(b_id,b_title,b_author,b_edition,b_publisher,"
                        + "b_description,b_pagecount,b_startdate,b_expiredate,b_acce_no,b_ddc_no"
                        + ",b_year,b_authormark,b_status,b_location)"
                        + " values(" + id + ",'" + title + "','" + author + "','" + b_edition 
                        + "','" + publisher + "','" + desc + "'," + page + "," + sdate + "," 
                        + exdate + "," + acc + "," + ddc + "," + year + ",'" + b_authormark + "',1,'Shelf1')";
                stm = sql.createStatement();
                stm.execute(query);
                JOptionPane.showMessageDialog(null, "Record Added!");
                id += 1;
                jTextFieldID.setText(id + "");
                jTextFieldaccno.setText("");
                jTextFieldauth_mark.setText("");
                jTextFieldauthor.setText("");
                jTextFieldddc_no.setText("");
                jTextFielddesc.setText("");
                jTextFieldedition.setText("");
                jTextFieldexdate.setText("");
                jTextFieldpages.setText("");
                jTextFieldpublisher.setText("");
                jTextFieldsdate.setText("");
                jTextFieldtitle.setText("");
                jTextFieldyear.setText("");
            } else {
                JOptionPane.showMessageDialog(null, "Fill all the records!");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }


    }//GEN-LAST:event_jButtonsaveActionPerformed

