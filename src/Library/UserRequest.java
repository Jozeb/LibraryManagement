    private void jTable_RequestMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_RequestMouseClicked
        try {
            // TODO add your handling code here:

            int row = jTable_Request.getSelectedRow();

            String user_id = jTable_Request.getModel().getValueAt(row, 0).toString();
            String name = jTable_Request.getModel().getValueAt(row, 1).toString();

            String query = "Select user_fname, user_contact, user_telnum, user_address, user_email "
                    + "from user "
                    + "where user_id = '" + user_id + "'";

            statement = connection.createStatement();
            resultSet = statement.executeQuery(query);
            String fname = "", contact = "", telno = "", address = "", email = "";

            if (resultSet.next()) {
                fname = resultSet.getString("user_fname");
                contact = resultSet.getString("user_contact");
                telno = resultSet.getString("user_telnum");
                address = resultSet.getString("user_address");
                email = resultSet.getString("user_email");
            }

            jLabel_email.setText(email);
            jLabel_name.setText(name);
            jLabel_fname.setText(fname);
            jLabel_contact.setText(contact);
            jLabel_requestID.setText(user_id);
            jLabel_contact.setText(contact);
            jLabel_telno.setText(telno);
            jLabel_address.setText(address);

        } catch (SQLException ex) {
            Logger.getLogger(UserRequest.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable_RequestMouseClicked

