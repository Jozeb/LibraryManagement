    private void jText_useridKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jText_useridKeyReleased
        try {
            // TODO add your handling code here:
            
            String userid = jText_userid.getText(); 
            String query = "select user_name from user where user_id = '"+userid+"'";
           
            statement = connection.createStatement();
            resultSet = statement.executeQuery(query);
                 
           jText_username.setText("Invalid User ID");
            isUserReady = false;

            if (resultSet.next()) {
                jText_username.setText(resultSet.getString("user_name"));
                isUserReady = true;
            }

             
               
            
        } catch (SQLException ex) {
           // Logger.getLogger(AddBookRequest.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_jText_useridKeyReleased

    private void jButton_acceptMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_acceptMouseClicked
        try {
            // TODO add your handling code here:
            
            String title = jText_title.getText();
            String author = jText_author.getText();
            String edition = jText_edition.getText();
            String user_id = jText_userid.getText();
            
            String query = "Insert into book_request (user_id, b_title, b_author, b_edition, request_date, request_status) "
                    + "values ('"+user_id+"', "
                    + "'"+title+"', "
                    + "'"+author+"', "
                    + "'"+edition+"', "
                    + "sysdate(), "
                    + "0)";
            
            System.out.println(query);
            if(!title.isEmpty() || !author.isEmpty()){
            
            if (isUserReady) {
                statement = connection.createStatement();
                statement.execute(query);
                Close();
            }else{
                JOptionPane.showMessageDialog(null, "Enter user  id");
            }
            
            }else{
                JOptionPane.showMessageDialog(null, "Title or Author\ncannot be empty");
            }

        } catch (SQLException ex) {
            Logger.getLogger(AddBookRequest.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
    }//GEN-LAST:event_jButton_acceptMouseClicked

