    private void jButtonsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonsaveActionPerformed
        try {
            // TODO add your handling code here:
            int page,ddc,year;
             
            String title=jTextFieldtitle.getText();
            System.out.println("acc = " + title);
            String author=jTextFieldauthor.getText();
            String b_b_edition=jTextFieldedition.getText();
            String publisher=jTextFieldpublisher.getText();
            String desc=jTextFielddesc.getText();
            String sdate=jTextFieldsdate.getText();
            String exdate = jTextFieldexdate.getText();
            if(!"".equals(jTextFieldaccno.getText()) && !"".equals(jTextFieldauth_mark.getText())&&!"".equals(jTextFieldddc_no.getText())){
                page=Integer.parseInt(jTextFieldpages.getText());
                acc = Integer.parseInt(jTextFieldaccno.getText());
                ddc=Integer.parseInt(jTextFieldddc_no.getText());
                year=Integer.parseInt(jTextFieldyear.getText());
                String b_authormark=jTextFieldauth_mark.getText();
                
                String query="update Books set b_title = '"+title+"',b_author='"+author+"',b_edition= '"+b_b_edition+"',b_publisher = '"+publisher+"',"
                        + "b_description = '"+desc+"',b_pagecount = "+page+",b_acce_no= "+acc+",b_startdate = '"+sdate+"',"
                        + "b_expiredate = '"+exdate+"',b_ddc_no = "+ddc+",b_year = "+year+",b_authormark='"+b_authormark+"' where b_acce_no = "+acc+"";
                
                stm=sql.createStatement();
                stm.execute(query);
                JOptionPane.showMessageDialog(null, "Record updated!");
                close();
            }
            else{
                JOptionPane.showMessageDialog(null, "Fill all the records!");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_jButtonsaveActionPerformed

