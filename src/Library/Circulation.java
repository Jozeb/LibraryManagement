    private void jTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableMouseClicked
        // TODO add your handling code here:

        try {
            // TODO add your handling code here:

            int row = jTable.getSelectedRow();

            String issue_id = jTable.getModel().getValueAt(row, 0).toString();
            String user_name = jTable.getModel().getValueAt(row, 1).toString();
            String book_name = jTable.getModel().getValueAt(row, 2).toString();
            String issue_date = jTable.getModel().getValueAt(row, 3).toString();

            String query = "Select i.return_date, i.user_id, b.b_author from book_issue i, book b "
                    + "where i.b_id = b.b_id "
                    + "and issue_id = " + issue_id;

            statement = connection.createStatement();
            resultSet = statement.executeQuery(query);

            if (resultSet.next()) {
                String return_date = resultSet.getString("return_date");
                if (return_date == null) {
                    return_date = "N/A";
                    jButton_receive.setEnabled(true);
                }else{
                    jButton_receive.setEnabled(false);
                }
                String user_id = resultSet.getString("user_id");
                String author = resultSet.getString("b_author");

                jLabel_userid.setText(user_id);
                jLabel_username.setText(user_name);
                jLabel_bookname.setText(book_name);
                jLabel_author.setText(author);
                jLabel_issuedate.setText(issue_date);
                jLabel_returndate.setText(return_date);

            }

        } catch (SQLException ex) {
            Logger.getLogger(UserRequest.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTableMouseClicked

