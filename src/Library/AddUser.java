    private void jText_userIDKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jText_userIDKeyReleased
        // TODO add your handling code here:
        try {
            String user_id = jText_userID.getText();

            if (user_id.length() == 7) {

                String query = "Select user_name from user where user_id = '" + user_id + "'";

                statement = connection.createStatement();
                resultSet = statement.executeQuery(query);

                if (resultSet.next()) {
                    user_ready = false;
                    jButtonsave.setEnabled(false);
                    jMessageBox.setText("Invalid User already exists");
                    jText_UserName.setText(resultSet.getString("user_name"));
                } else {
                    jButtonsave.setEnabled(true);
                    user_ready = true;
                    jMessageBox.setText(" ");
                    jText_UserName.setText("");

                }

            } else {
                jButtonsave.setEnabled(false);
                user_ready = false;
                jMessageBox.setText("Invalid User ID");
            }

        } catch (SQLException ex) {
            Logger.getLogger(AddUser.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jText_userIDKeyReleased

    private void jPass1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPass1KeyReleased
        // TODO add your handling code here:

        String pass1 = jPass1.getText();
        String pass2 = jPass2.getText();
        if (pass1.length() > 4) {

            if (pass1.compareTo(pass2) == 0) {
                jButtonsave.setEnabled(true);
                pass_ready = true;
                jMessageBox.setText("Passwords Match");
            } else {
                jButtonsave.setEnabled(false);
                pass_ready = false;
                jMessageBox.setText("Passwords do not Match");
            }
        } else {
            jButtonsave.setEnabled(false);
            pass_ready = false;
            jMessageBox.setText("Password should be atleast 5 characters");
        }


    }//GEN-LAST:event_jPass1KeyReleased

