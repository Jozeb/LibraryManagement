    public void update_table() throws ClassNotFoundException{
        try {
            String que = "Select user_id as UserID, user_name as Name, user_fname as FatherName from user "
                    + "where user_status = 1 ";

            String filter = jTextFieldSearch.getText();

            if (filter.length() > 0) {

                int type = jCombo_filter.getSelectedIndex();

                switch (type) {
                    case 0:
                     que += "and user_id like '%"+filter+"%'";
                        break;
                    
                    case 1:
                        que += "and user_name like '%"+filter+"%'";
                        break;
                }

            }

            state = conn.createStatement();
            set = state.executeQuery(que);
            jTableusers.setModel(DbUtils.resultSetToTableModel(set));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
        
    }
