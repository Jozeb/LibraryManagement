    private void jText_useridKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jText_useridKeyReleased
        try {
            // TODO add your handling code here:
            
            String userid = jText_userid.getText();
            String query = "Select user_name, user_status from user where user_id  = '"+userid+"'";
            
            statement = connection.createStatement();
            resultSet = statement.executeQuery(query);

            if (resultSet.next()) {
                String username = resultSet.getString("user_name");
                jLabel_username.setText(username);
                if(resultSet.getString("user_status").compareTo("1")==0){
                user_ready = true;    
                }else{
                jLabel_username.setText("User Request Pending");
                user_ready = true;
                }
                
            } else {
                jLabel_username.setText("Invalid User ID");
                user_ready = false;
            }
        } catch (SQLException ex) {
            Logger.getLogger(BookIssue.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jText_useridKeyReleased

    private void jText_accnoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jText_accnoKeyReleased
        // TODO add your handling code here:
        
        try {
            // TODO add your handling code here:
            
            String accno = jText_accno.getText();
            String query = "Select b_title, b_author, b_edition, b_status from book where b_acce_no = '"+accno+"'";
            
            statement = connection.createStatement();
            resultSet = statement.executeQuery(query);

            if (resultSet.next()) {
                String title = resultSet.getString("b_title");
                String author = resultSet.getString("b_author");
                String edition = resultSet.getString("b_edition");
                String status = resultSet.getString("b_status");
                
                jLabel_bookname.setText(title);
                jLabel_author.setText(author);
                jLabel_edition.setText(edition);
                if(status.compareTo("1")==0){
                book_ready = true;
                jLabel_msg.setVisible(false);
                jButton2.setEnabled(true);
                }else{
                jLabel_msg.setVisible(true);
                jButton2.setEnabled(false);
            }
                
            } else {
                
                jLabel_bookname.setText("Invalid Accno");
                jLabel_author.setText(" ");
                jLabel_edition.setText(" ");
                book_ready = false;
            }
        } catch (SQLException ex) {
            Logger.getLogger(BookIssue.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jText_accnoKeyReleased

